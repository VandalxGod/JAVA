
public class JavaBasic {
    public static void main(String[] args){
        int a = 10;
        int b = ++a; // pre-increment
        System.out.println(a);
        System.out.println(b);

        int c = 15;
        int d = --c; // pre-decrement
        System.out.println(c);
        System.out.println(d);
    }
    
}
