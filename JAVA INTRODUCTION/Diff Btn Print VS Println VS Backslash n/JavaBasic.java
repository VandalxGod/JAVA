

public class JavaBasic {
    public static void main(String[] args){

        // Using print
        System.out.print("Hello");
        System.out.print(" World");

        // Using println
        System.out.println("Hello");
        System.out.println("World");

        // Using newline character \n
        System.out.print("Hello\n");
        System.out.print("World");
    }
    
}
